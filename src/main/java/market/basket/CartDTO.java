package market.basket;


public class CartDTO {

	private String cart_no;
	private int count_num;
	private int total_price;
	private String product_no;
	private String id;
	private String product_sfile;
	private String milage;
	private String price;
	private String product_name;

	
	public String getProduct_sfile() {
		return product_sfile;
	}
	public void setProduct_sfile(String product_sfile) {
		this.product_sfile = product_sfile;
	}
	public String getMilage() {
		return milage;
	}
	public void setMilage(String milage) {
		this.milage = milage;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getCart_no() {
		return cart_no;
	}
	public void setCart_no(String cart_no) {
		this.cart_no = cart_no;
	}
	public int getCount_num() {
		return count_num;
	}
	public void setCount_num(int count_num) {
		this.count_num = count_num;
	}
	
	public int getTotal_price() {
		return total_price;
	}
	public void setTotal_price(int total_price) {
		this.total_price = total_price;
	}
	public String getProduct_no() {
		return product_no;
	}
	public void setProduct_no(String product_no) {
		this.product_no = product_no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
	
}
	
